/** Helper: find any course by slug */
export function getCourseBySlug(slug) {
  return [...itCourses, ...beautyCourses].find(c => c.slug === slug) || null;
}

/** Helper: get category label for a course */
export function getCourseCategory(course) {
  if (itCourses.some(c => c.id === course.id)) return 'IT Courses';
  if (beautyCourses.some(c => c.id === course.id)) return 'Female Beauty Courses';
  return 'Course';
}

/** Helper: get related courses (same category, excluding current) */
export function getRelatedCourses(course, limit = 4) {
  const pool = itCourses.some(c => c.id === course.id) ? itCourses : beautyCourses;
  return pool.filter(c => c.id !== course.id).slice(0, limit);
}
